"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também
como um tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado.

#OBS: O tipo None é sempre especificado com a primeira letra maiúscula.

Certo: None
Errado: none

Quando utilizar?

- Podemos utlizar None quando queremos criar uma variável
e inicializá-la com um tipo sem tipo, antes de recebermos um valor final.
"""


numero = None

print(numero)
print(type(numero))

numero = 1.45, 1.57, 6.89

print(numero)
print(type(numero))

# OBS: O tipo None sempre será 'falso' no Python.